package com.sergisvk.tema9.Ejercicio11;

public class Grupo {
    private int codigo;
    private String nombre;

    public Grupo(int codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    public int getCodigo() {
        return codigo;
    }

    public String getNombre() {
        return nombre;
    }
}

